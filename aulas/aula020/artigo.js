/*
const = descreve uma variável que não pode ser reatribuida (com o operador de atribuição), ex:

const nome = 'luiz';
nome = 'joão'; // Erro: Assigment to constant variable.

-

Variável = apelido para um valor salvo na memória

-

Valores = dados que realmente ficam salvos na memória e sustentam determinado tipo

-

Valores imutáveis = number, string, boolean, undefined, null, symbol e bight (primitivos)

-

Valores mutáveis = geralmente são estruturas de dados mais complexas que sustentam outros valores ou comportamentos internamente. Como é o caso do array, que pode ser composto por vários outros tipos de dados.

-

Junção de variáveis imutáveis com valores mutáveis: quando usamos const com valores do tipo primitivo, esse valor nunca mais poderá ser alterado, tanto pela característica da variável, como também a do valor que é de tipo imutável.
    Porém ao usamos essa variável com valores que são de origem mutáveis, como
por exemplo Arrays e objetos
*/